
public class Local {
	private String nome;
	private int km;

	public Local(String nome, int km) {
		this.nome = nome;
		this.km = km;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getKm() {
		return km;
	}

	public void setKm(int km) {
		this.km = km;
	}

}
